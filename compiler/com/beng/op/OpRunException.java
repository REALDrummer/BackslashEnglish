package com.beng.op;

public class OpRunException extends Exception {

	public OpRunException() {
		super();
	}

	public OpRunException(String message) {
		super(message);
	}

	public OpRunException(Throwable cause) {
		super(cause);
	}

	public OpRunException(String message, Throwable cause) {
		super(message, cause);
	}

}
